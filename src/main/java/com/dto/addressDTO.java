package com.dto;

import java.util.ArrayList;

public class addressDTO {
	
	private int num;
	
	private String name;
	
	ArrayList<addressDTO> dto;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ArrayList<addressDTO> getDto() {
		return dto;
	}

	public void setDto(ArrayList<addressDTO> dto) {
		this.dto = dto;
	}
	
	
	

}
